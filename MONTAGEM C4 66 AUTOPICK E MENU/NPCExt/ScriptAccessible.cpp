INT32 nCreateAcademy = 0;
bool CreateAcademy(CSocket *pSocket, char *pPacket, int PacketID, int Lenght)
{
	NPC_SHARED_GUARD(nCreateAcademy);
	TLS_TRACE_BEGIN;

	DWORD	nUserObjectId,
			nUserIndex,
			nNpcIndex,
			nReply,
			nID;

	CSharedData* pSD = NULL;
	CNPC* pNPC = NULL;
	CSharedData *pSDNPC = NULL;

	Disassemble(pPacket,"dddd", &nUserObjectId, &nUserIndex, &nNpcIndex, &nReply);

	nID = ((INT32*)(*(INT32*)0x9D364B0))[nNpcIndex];
	pNPC = CNPC::Find(nID);
	if(pNPC)
	{
		if(nUserIndex != NULL && nUserIndex < *((DWORD*)0x77E570))
		{
			pSD = (CSharedData*)(*(INT64*)0x9D36458 + sizeof(CSharedData) * nUserIndex);
		}

		if(pSD && (BYTE)pSD->_dw0050 != 0 && (BYTE)pSD->nIsPlayer != 0)
		{
			pSDNPC = pNPC->pSD;

			int n = (BYTE)(pSDNPC->_n334 >> 1) ;

			if( n == 0)
			{
				NPCEvent* pEvent = CNPCEventFactory::CreateCreateAcademyEvent(pNPC, pSD, nReply);
				
				if(pEvent)
				{
					pNPC->HandleEvent(pEvent);
					//pEvent->Release(__FILE__, __LINE__, 1, 0);
				}
				else
				{
					CLog::Add(CLog::red, "[%s][%d] Event failed", __FILE__, __LINE__);
				}
			}
		}
		else
		{
			CLog::Add(CLog::red, "[%s][%d] something is abnormal in CreateAcademyPacket", __FILE__, __LINE__);
		}

		pNPC->Release(__FILE__, __LINE__, 0x0B, 0);
	}

	TLS_TRACE_END(false);
}

